let currentLang = 'pt';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    updatePageLanguage();
    if (window.updateFabricsLanguage) {
        window.updateFabricsLanguage(lang);
    }
}

function updatePageLanguage() {
    const t = translations[currentLang];

    // Update data attributes for dynamic content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Update placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });
}

function getCurrentTranslations() {
    return translations[currentLang];
}

// Initialize language from localStorage
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'pt';
    currentLang = savedLang;

    // Sync all language selectors
    const selectors = document.querySelectorAll('.lang-selector select');
    selectors.forEach(sel => {
        sel.value = savedLang;
        sel.addEventListener('change', (e) => {
            const val = e.target.value;
            setLanguage(val);
            // Sync all other selectors
            document.querySelectorAll('.lang-selector select').forEach(s => s.value = val);
        });
    });

    updatePageLanguage();
});
